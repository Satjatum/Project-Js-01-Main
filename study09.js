// 1. while
let aa = 1
while (aa <= 5) {
    console.log(aa)
    aa++
}

// 2. do-while
let bb = 1
do {
    console.log(bb)
    bb++
} while (bb <= 5)

// 3. for
for (let cc = 1; cc <= 5; cc++) {
    console.log(cc)
}